

// Do...while
//Execulta instruções até que a condição se torne falsa.
//Porém, a primeira execução sempre ocorre. 


function exemploDoWhile(){
    let num = 0; 

    do{
        console.log(num);
        num++;
    } while(num <= 5)
}

exemploDoWhile()
//0
//1
//2
//3
//4
//5


